function applyTranslations(lang) {
  const dict = translations[lang];
  if (!dict) return;

  const elements = document.querySelectorAll("[data-i18n]");
  elements.forEach((el) => {
    const key = el.getAttribute("data-i18n");
    const value = dict[key];
    if (!value) return;

    if (key === "footer.copy") {
      const year = new Date().getFullYear();
      el.textContent = value.replace("{year}", year.toString());
    } else {
      el.textContent = value;
    }
  });

  const placeholders = document.querySelectorAll("[data-i18n-placeholder]");
  placeholders.forEach((el) => {
    const key = el.getAttribute("data-i18n-placeholder");
    const value = dict[key];
    if (!value) return;
    el.setAttribute("placeholder", value);
  });

  const titleEl = document.querySelector("title[data-i18n='meta.title']");
  const descEl = document.querySelector(
    "meta[name='description'][data-i18n='meta.description']"
  );
  if (titleEl && dict["meta.title"]) titleEl.textContent = dict["meta.title"];
  if (descEl && dict["meta.description"]) {
    descEl.setAttribute("content", dict["meta.description"]);
  }

  document.documentElement.lang = lang === "pt" ? "pt-BR" : "en";
}

function initLanguageSwitcher() {
  const buttons = document.querySelectorAll(".lang-switch");
  const storedLang = window.localStorage.getItem("designer-landing-lang");
  const initialLang = storedLang === "en" ? "en" : "pt";

  applyTranslations(initialLang);
  updateLangButtons(buttons, initialLang);

  buttons.forEach((btn) => {
    btn.addEventListener("click", () => {
      const lang = btn.getAttribute("data-lang");
      if (!lang) return;
      applyTranslations(lang);
      updateLangButtons(buttons, lang);
      window.localStorage.setItem("designer-landing-lang", lang);
    });
  });
}

function updateLangButtons(buttons, activeLang) {
  buttons.forEach((btn) => {
    const lang = btn.getAttribute("data-lang");
    btn.classList.toggle("active", lang === activeLang);
  });
}

function initSmoothScroll() {
  const links = document.querySelectorAll('a[href^="#"]');
  links.forEach((link) => {
    link.addEventListener("click", (event) => {
      const href = link.getAttribute("href");
      if (!href || href === "#") return;
      const target = document.querySelector(href);
      if (!target) return;
      event.preventDefault();
      const headerOffset = 72;
      const rect = target.getBoundingClientRect();
      const offsetTop = rect.top + window.scrollY - headerOffset + 4;
      window.scrollTo({
        top: offsetTop,
        behavior: "smooth",
      });
    });
  });
}

function initMenuToggle() {
  const toggle = document.querySelector(".menu-toggle");
  const nav = document.querySelector(".main-nav");
  if (!toggle || !nav) return;

  toggle.addEventListener("click", () => {
    nav.classList.toggle("open");
  });

  nav.querySelectorAll("a").forEach((link) => {
    link.addEventListener("click", () => {
      nav.classList.remove("open");
    });
  });
}

function initYearFallback() {
  const yearSpan = document.getElementById("year");
  if (!yearSpan) return;
  yearSpan.textContent = String(new Date().getFullYear());
}

document.addEventListener("DOMContentLoaded", () => {
  initLanguageSwitcher();
  initSmoothScroll();
  initMenuToggle();
  initYearFallback();
});

